public class method1 {

    public static void numbering(int init, int limit){
        int i = init;
        while(i < limit){
            System.out.println(i);
            i++;
        }
    }
    public static void main(String[] args){
        numbering(1, 5);//순서도 지켜야 한다


    }
    //main 함수 부분에서 어떻게 실행하는 지 먼저 본 다음에 하기
}
